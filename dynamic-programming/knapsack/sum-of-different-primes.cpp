int n, k;
const int MAXN = 1121, MAXK = 15;
vector<vector<vector<int>>> memo (primes.size(), vector<vector<int>>(MAXN, vector<int> (MAXK)));

long long cnt = 0;

long long rec(int id, int remn, int remk){
    if (remn == 0 && remk == 0) return 1;
    if (id == primes.size() || remk == 0 || primes[id] > remn) return 0;
    int& ans = memo[id][remn][remk];
    if (ans != -1) return ans;
    return ans = rec(id+1, remn, remk) + rec(id+1, remn-primes[id], remk-1);
}

int main(){
    for (int i = 0; i < primes.size(); i++)
        for (int j = 0; j < MAXN; j++)
            for (int l = 0; l < MAXK; l++)
                memo[i][j][l] = -1;
    while (true){
        cin >> n >> k;
        if (n + k == 0) break;
        long long ways = rec(0, n, k);
        cout << ways << "\n";
    }
}